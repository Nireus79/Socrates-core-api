"""
Library Integrations API Router

Provides endpoints for accessing all 12 Socratic ecosystem libraries:
- socratic-learning: Learning analytics and recommendations
- socratic-analyzer: Code quality analysis
- socratic-conflict: Multi-agent conflict resolution
- socratic-knowledge: Enterprise knowledge management
- socratic-workflow: Workflow orchestration
- socratic-docs: Documentation generation
- socratic-performance: Performance monitoring
"""

import logging
from typing import Any, Dict, List, Optional

from fastapi import APIRouter, HTTPException, Query

logger = logging.getLogger(__name__)

router = APIRouter(prefix="/libraries", tags=["Library Integrations"])


# ============================================================================
# ENDPOINTS - Code Analysis
# ============================================================================


@router.post("/analyzer/analyze-code")
async def analyze_code_quality(
    code: str = Query(..., description="Source code to analyze"),
    filename: str = Query("code.py", description="Filename for context"),
) -> Dict[str, Any]:
    """
    Analyze code quality using socratic-analyzer.

    Uses static analysis to check for:
    - Code quality metrics (0-100 score)
    - Security issues
    - Performance concerns
    - Architecture insights

    Args:
        code: Source code to analyze
        filename: Filename (for context)

    Returns:
        Dictionary with analysis results including quality_score, issues, recommendations
    """
    try:
        from socratic_system.orchestration.orchestrator import AgentOrchestrator

        # Get orchestrator from app state (would need to be passed as dependency in real implementation)
        # For now, creating a minimal instance
        orchestrator = AgentOrchestrator(api_key_or_config="placeholder")

        result = orchestrator.analyze_code_quality(code, filename)
        if result:
            return result
        else:
            raise HTTPException(
                status_code=503,
                detail="Code analyzer is not available. Is socratic-analyzer installed?"
            )
    except Exception as e:
        logger.error(f"Code analysis failed: {e}")
        raise HTTPException(status_code=500, detail=str(e))


