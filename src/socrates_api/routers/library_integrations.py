@router.post("/learning/log-interaction")
async def log_learning_interaction(
    session_id: str = Query(..., description="Learning session ID"),
    agent_name: str = Query(..., description="Agent name"),
    input_data: Dict[str, Any] = Query(..., description="Input to agent"),
    output_data: Dict[str, Any] = Query(..., description="Output from agent"),
    tokens_used: int = Query(0, description="LLM tokens used"),
    cost: float = Query(0.0, description="Cost in USD"),
    duration_ms: float = Query(0.0, description="Duration in milliseconds"),
    success: bool = Query(True, description="Was interaction successful?"),
    tags: Optional[List[str]] = Query(None, description="Tags for categorization"),
) -> Optional[Dict[str, Any]]:
    """
    Log a learning interaction using socratic-learning.

    Tracks agent interactions for:
    - Learning progress analytics
    - Concept mastery assessment
    - Misconception detection
    - Personalized recommendations

    Args:
        session_id: Learning session identifier
        agent_name: Name of the agent that executed
        input_data: Input provided to the agent
        output_data: Output generated by the agent
        tokens_used: LLM tokens consumed
        cost: Cost in USD
        duration_ms: Execution duration
        success: Whether execution was successful
        tags: Optional tags for analysis

    Returns:
        Interaction log entry or None if learning system unavailable
    """
    try:
        from socratic_system.orchestration.orchestrator import AgentOrchestrator

        orchestrator = AgentOrchestrator(api_key_or_config="placeholder")
        result = orchestrator.log_learning_interaction(
            session_id=session_id,
            agent_name=agent_name,
            input_data=input_data,
            output_data=output_data,
            tokens=tokens_used,
            cost=cost,
            duration_ms=duration_ms,
            success=success,
        )
        return result or {"status": "logged"}
    except Exception as e:
        logger.error(f"Failed to log learning interaction: {e}")
        raise HTTPException(status_code=500, detail=str(e))


